/* Set the width of the side navigation to 250px */
function openNav() {
    let sidenav = document.getElementById("mySidenav");
  sidenav.classList.add("active");
}

/* Set the width of the side navigation to 0 */
function closeNav() {
    let sidenav = document.getElementById("mySidenav");
  sidenav.classList.remove("active");
}